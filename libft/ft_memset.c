#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char *str;

	str = (unsigned char *)s;
	while (str && n > 0)
	{
		*str = (unsigned char)c;
		n--;
		str++;
	}
	return (s);
}
